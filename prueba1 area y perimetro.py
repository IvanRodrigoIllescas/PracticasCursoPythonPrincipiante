def area_triangulo(base, alto):

    '''numero, numero --> numero

    Devuelve el area del triangulo


    >>>area_triangulo (5, 10)
    25
    
    >>>area_triangulo (7.23, 9.54)
    34.4871'''
    
    area=(base*alto)/2
    return area

def perimetro_triangulo (a,b,c): 
    perimetro = (a+b+c)
    return perimetro
